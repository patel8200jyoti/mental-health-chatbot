import logging
logger = logging.getLogger(__name__)


app = APIRouter(prefix='/chat',tags=['Chat'])


# Create chat
@app.post("/")
async def create_chat(user = Depends(get_current_user)):
    chat = Chats(
        user_id=user['user_id'],
        chat_id=str(uuid4()),
        chat_title='hehe',
        created_at=datetime.utcnow()
    )
    
    db = await get_database()
    
    await db.chats.insert_one(chat_entity(chat.model_dump()))
    logger.info("Chat Created - chat id ")
    return {"message": "Chat created", "chat_id": chat.chat_id}



